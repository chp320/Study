/* 반복문을 이용하여 구구단(15단) 출력하기
1 * 1 = 1
1 * 2 = 2
..
1 * 8 = 8
1 * 9 = 9

2 * 1 = 2	3 * 1 = 3
2 * 2 = 4	3 * 2 = 6
..
2 * 8 = 16	3 * 8 = 24
2 * 9 = 18	3 * 9 = 27

4 * 1 = 4	5 * 1 = 5	6 * 1 = 6
4 * 2 = 8	5 * 2 = 10	6 * 2 = 12
..
4 * 8 = 32	5 * 8 = 40	6 * 8 = 48
4 * 9 = 36	5 * 9 = 45	6 * 9 = 54
..
*/

#include <iostream>
using namespace std ;

int main()
{
	for(int i=1; i<=9; i++)
	{
		for(int j=1; j<=9; j++)
		{
			cout << i << " * " << j << " = " << i*j << endl ;
		}
	}
	
	return 0 ;
}